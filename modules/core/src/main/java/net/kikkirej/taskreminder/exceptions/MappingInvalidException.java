package net.kikkirej.taskreminder.exceptions;

public class MappingInvalidException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6055377689978392309L;

	public MappingInvalidException() {
	}

	public MappingInvalidException(String arg0) {
		super(arg0);
	}

	public MappingInvalidException(Throwable arg0) {
		super(arg0);
	}

	public MappingInvalidException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MappingInvalidException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
